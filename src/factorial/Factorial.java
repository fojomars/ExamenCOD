package factorial;

public class Factorial {

    public static void main(String[] args) {

        int x; // Cambie la variable j por x
        int y; // Cambie el nombre de la variable f por y

        x = 8;

        int z; // Cambie el nombre de la variable i por z
        if (x == 0) {
            y = 1;
        } else {
            y = 1;
            for (z = x; z >= 1; z--) {
                y = y * z;
            }
        }

        System.out.println(y);
// Terminado
    }

}
